// Define an array to store events
let events = [];

// letiables to store event input fields and reminder list
let eventDateInput =
	document.getElementById("eventDate");
let eventPositionInput =
	document.getElementById("position");
let eventEmployeeInput =
	document.getElementById("employee");
let eventStartTimeInput = document.getElementById("eventStartTime");
let eventEndTimeInput = document.getElementById("eventEndTime");
let shiftList = document.getElementById("shiftList");

// Counter to generate unique event IDs
let eventIdCounter = 1;

function formatTime12Hour(timeStr) {
    if (!timeStr) return "";
    let [hour, minute] = timeStr.split(":");
    hour = parseInt(hour);
    let ampm = hour >= 12 ? "PM" : "AM";
    hour = hour % 12;
    if (hour === 0) hour = 12;
    return `${hour}:${minute} ${ampm}`;
}

// Function to add events
function addEvent() {
	let date = eventDateInput.value;
    let startTime = formatTime12Hour(eventStartTimeInput.value);
    let endTime = formatTime12Hour(eventEndTimeInput.value);
	let position = eventPositionInput.options[eventPositionInput.selectedIndex].text;
	let employee = eventEmployeeInput.options[eventEmployeeInput.selectedIndex].text;

	if (date && employee && position) {
		// Create a unique event ID
		let eventId = eventIdCounter++;

		events.push(
			{
				id: eventId, 
                date: date,
                startTime: startTime, 
                endTime: endTime,
				position: position,
				employee: employee
			}
		);
		showCalendar(currentMonth, currentYear);
		eventDateInput.value = "";
        eventStartTimeInput.value = "";
        eventEndTimeInput.value = "";
		eventPositionInput.value = "";
		eventEmployeeInput.value = "";
		displayShifts();
	}
}

// Function to delete an event by ID
function deleteEvent(eventId) {
	// Find the index of the event with the given ID
	let eventIndex =
		events.findIndex((event) =>
			event.id === eventId);

	if (eventIndex !== -1) {
		// Remove the event from the events array
		events.splice(eventIndex, 1);
		showCalendar(currentMonth, currentYear);
		displayShifts();
	}
}

// Function to display reminders
function displayShifts() {
	shiftList.innerHTML = "";
	for (let i = 0; i < events.length; i++) {
		let event = events[i];
		let eventDate = new Date(event.date +"T00:00");
		if (eventDate.getMonth() ===
			currentMonth &&
			eventDate.getFullYear() ===
			currentYear) {
			let listItem = document.createElement("li");
			listItem.innerHTML =
				`<strong>${event.position}</strong> - 
			${event.employee} on 
			${eventDate.toLocaleDateString()}
            (${event.startTime} - ${event.endTime})`;

			// Add a delete button for each reminder item
			let deleteButton =
			document.createElement("button");
			deleteButton.className = "delete-event";
			deleteButton.textContent = "Delete";
			deleteButton.onclick = function () {
				deleteEvent(event.id);
			};

			listItem.appendChild(deleteButton);
			shiftList.appendChild(listItem);
		}
	}
}

// Function to generate a range of 
// years for the year select input
function generate_year_range(start, end) {
	let years = "";
	for (let year = start; year <= end; year++) {
		years += "<option value='" +
			year + "'>" + year + "</option>";
	}
	return years;
}

